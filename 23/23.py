# задача: https://inf-ege.sdamgia.ru/problem?id=3607

def f(n):
    """
    n - число

    пример работы функции:
    ввод - "f(2)"
    вывод - "7"
    """

    if n > 50:
        # не подходит под условия
        return 0

    elif n == 50:
        # нужное число
        return 1

    # суммируем все варианты дальнейший действий
    return f(n + 2) + f(n * 5)


print(f(2))


