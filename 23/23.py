# задача: https://inf-ege.sdamgia.ru/problem?id=11358

def f(n, is_10_in):
    """
    n - число
    is_10_in - проверка на содержание в траектории высислений числа 10 (переменная, пройдена ли проверка)

    пример работы функции:
    ввод - "f(3, 0)"
    вывод - "60"
    """

    if n > 12:
        # не подходит под условия
        return 0

    elif n == 12:
        # если проверка пройдена, вернём "1", иначе "0"
        return is_10_in

    elif n == 10:
        is_10_in = 1

    # суммируем все варианты дальнейший действий
    return f(n + 1, is_10_in) + f(n + 2, is_10_in) + f(n * 2, is_10_in)


print(f(3, 0))  # 0 - проверка ещё не пройдена


